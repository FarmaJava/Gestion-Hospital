class Medico:

    def __init__(
        self,
        id_usuario,
        nombre,
        apellido,
        matricula,
        especialidad,
        telefono,
        email
    ):
        self.id_usuario = id_usuario
        self.nombre = nombre
        self.apellido = apellido
        self.matricula = matricula
        self.especialidad = especialidad
        self.telefono = telefono
        self.email = email